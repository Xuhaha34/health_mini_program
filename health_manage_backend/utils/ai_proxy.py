# 6. 外部大模型调用（统一的 OpenAI Chat Completions 兼容接口）
# ============================================================================
def _call_external_model(message: str, history: list = None, user_profile: dict = None) -> Optional[str]:
    """调用外部大模型。失败返回 None，由 caller 做降级处理"""
    if not AI_API_KEY or not AI_API_URL:
        return None

    # —— 构造系统提示词（如含用户体征数据，则加入个性化信息）——
    system_content = SYSTEM_PROMPT
    if user_profile:
        parts = []
        for k, v in user_profile.items():
            if v not in (None, "", []):
                parts.append(f"{k}: {v}")
        if parts:
            system_content = (
                "你是一个专业的健康管理助手。当前用户的基本体征数据：\n"
                + "\n".join(f"• {p}" for p in parts)
                + "\n\n请结合以上数据给出个性化建议，用中文回答，字数控制在150-300字。"
                "如涉及医疗诊断，请建议用户咨询专业医生。"
            )

    messages = [{"role": "system", "content": system_content}]
    if history:
        messages.extend(history)
    messages.append({"role": "user", "content": message})

    try:
        response = requests.post(
            AI_API_URL,
            headers={
                "Authorization": f"Bearer {AI_API_KEY}",
                "Content-Type": "application/json",
            },
            json={
                "model": AI_MODEL,
                "messages": messages,
                "max_tokens": AI_MAX_TOKENS,
                "temperature": AI_TEMPERATURE,
            },
            timeout=AI_TIMEOUT,
        )
        response.raise_for_status()
        result = response.json()
        return result["choices"][0]["message"]["content"].strip()
    except requests.exceptions.RequestException as e:
        logger.warning(f"外部AI接口调用失败（{AI_PROVIDER}），回落本地回复: {str(e)}")
        return None
    except (KeyError, ValueError) as e:
        logger.warning(f"外部AI返回格式异常: {str(e)}")
        return None


# ============================================================================
# 7. 主入口：外部大模型 + 本地回退
# ============================================================================
def call_ai_api(message: str, history: list = None, user_profile: dict = None) -> Dict[str, Any]:
    """
    调用AI接口进行健康问答。

    :param message: 用户问题
    :param history: 历史对话上下文，例如 [{"role": "user", "content": "..."}, {"role": "assistant", "content": "..."}]
    :param user_profile: 用户体征数据，例如 {"体重": "70kg", "血压": "135/85", "年龄": 35, "性别": "男"}
    :return: dict，包含 reply / provider / model / from_external 字段
    """
    reply = None

    # —— 1. 尝试外部大模型（仅在有 API KEY 且未选 local 时）——
    if AI_PROVIDER != "local" and AI_API_KEY and AI_API_URL:
        reply = _call_external_model(message, history, user_profile)
        if reply:
            return {
                "reply": reply,
                "provider": AI_PROVIDER,
                "model": AI_MODEL,
                "from_external": True,
            }

    # —— 2. 本地智能回复（Fallback）——
    local_reply = _local_smart_reply(message)
    return {
        "reply": local_reply,
        "provider": "local",
        "model": "local-kb",
        "from_external": False,
    }


