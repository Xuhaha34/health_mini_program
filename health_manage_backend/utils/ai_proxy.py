def _get_config(key: str, default: str = "") -> str:
    """优先读环境变量，其次读 Django settings，否则返回默认值"""
    env_val = os.getenv(key, "").strip()
    if env_val:
        return env_val
    try:
        from django.conf import settings
        return getattr(settings, key, default) or default
    except Exception:
        return default

